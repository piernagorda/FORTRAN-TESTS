C     PROGRAM FOR FINDING THE LARGEST VALUE
C   X     ATTAINED BY A SET OF NUMBERS
      DIMENSION A(30)
C     FREQUENCY 30(2,1,10), 5(100) 
C     NOT ACCEPTED IN MODERN COMPILERS
      READ 1, N, (A(I), I=1,N)
  1   FORMAT (I3/(12F6.0))
      BIGA = A(1)
  5   DO 20 I = 2,N
  30  IF (BIGA-A(I)) 10,20,20
  10  BIGA = A(I)
  20  CONTINUE
      PRINT 2, N, BIGA
  2   FORMAT (22H1 THE LARGEST OF THESE I3, 12H NUMBERS IS 12F6.0)
      END
